// Export a helper function to get case study by ID
export const getCaseStudyById = (
  id: string | number
): CaseStudy | undefined => {
  console.log("Searching for case with ID:", id);
  console.log(
    "Available case IDs:",
    caseStudies.map((c) => c.id)
  );

  // Check if id is a string and try to normalize it (lowercase, trim)
  const normalizedId = typeof id === "string" ? id.trim().toLowerCase() : id;

  // Search with exact match first
  let foundCase = caseStudies.find((c) => c.id === id);

  // If not found, try with normalized id
  if (!foundCase && typeof id === "string") {
    foundCase = caseStudies.find(
      (c) => typeof c.id === "string" && c.id.toLowerCase() === normalizedId
    );
  }

  console.log("Found case:", foundCase?.id);
  return foundCase;
};
